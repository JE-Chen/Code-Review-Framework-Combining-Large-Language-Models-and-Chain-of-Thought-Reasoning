def fmt_of(sub, exact=False):
    p = next(
        x for x in doc.paragraphs
        if ((x.text.strip() == sub) if exact else (sub in x.text))
    )
    ppr = p._p.find(qn("w:pPr"))
    rpr = next((r._element.find(qn("w:rPr")) for r in p.runs if r.text.strip()), None)
    return ppr, rpr


# 模板格式：body 14pt 正文。標題不重寫故毋需 h2/h3 模板。
T = {"body": fmt_of("程式碼審查（Code Review）是一個在軟體開發流程中極為重要的環節")}


def has_img(p):
    return bool(p._p.findall(".//" + qn("w:drawing")))


def _new_body_after(cursor_el, text):
    new_p = OxmlElement("w:p")
    cursor_el.addnext(new_p)
    ppr, rpr = T["body"]
    if ppr is not None:
        new_p.append(copy.deepcopy(ppr))
    para = Paragraph(new_p, parent)
    run = para.add_run(text)
    if rpr is not None:
        run._element.insert(0, copy.deepcopy(rpr))
    return new_p


def _delete_text_paras(paras):
    """刪除純文字段（含圖 / 表之段保留）。"""
    for p in paras:
        if has_img(p):
            continue
        p._p.getparent().remove(p._p)


def replace_between(after_sub, before_sub, items):
    """以章節標題為錨：刪除 after_sub 之後、before_sub 之前的所有文字段（保留圖 / 表），
    再於 after_sub 後依序插入 items（皆 body）。"""
    paras = doc.paragraphs
    ai = next(i for i, p in enumerate(paras) if after_sub in p.text)
    bi = next(i for i, p in enumerate(paras) if before_sub in p.text and i > ai)
    _delete_text_paras(paras[ai + 1:bi])
    cursor = paras[ai]._p
    for text in items:
        cursor = _new_body_after(cursor, text)
    print(f"OK between〔{after_sub[:14]}…〕 +{len(items)} 段")


def _set_para_text(p, text):
    """清掉段內所有 run（保留 pPr / 段落格式），寫入單一 run 並沿用首個非空 run 之 rPr。"""
    rpr = next((r._element.find(qn("w:rPr")) for r in p.runs if r.text.strip()), None)
    for r in list(p.runs):
        r._element.getparent().remove(r._element)
    run = p.add_run(text)
    if rpr is not None:
        run._element.insert(0, copy.deepcopy(rpr))


def replace_para(orig_sub, new_texts, after_only=False):
    """外科式：定位含 orig_sub 之段，after_only=False 時改寫其文字為 new_texts[0]，其餘
    new_texts 逐段插入其後（after_only=True 時整串 new_texts 全部插入其後、原段不動）。
    不觸及任何圖 / 圖說。"""
    p = next(x for x in doc.paragraphs if orig_sub in x.text)
    if after_only:
        cursor = p._p
        for text in new_texts:
            cursor = _new_body_after(cursor, text)
    else:
        _set_para_text(p, new_texts[0])
        cursor = p._p
        for text in new_texts[1:]:
            cursor = _new_body_after(cursor, text)
    print(f"OK para〔{orig_sub[:14]}…〕 ×{len(new_texts)} 段"
          f"{'(append)' if after_only else ''}")


